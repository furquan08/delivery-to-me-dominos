package com.jos.dem.springboot.cucumber.model;

public class User{
	private String gender;
	private String name;
	private int id;
	private String email;
	private String status;

	public String getGender(){
		return gender;
	}

	public String getName(){
		return name;
	}

	public int getId(){
		return id;
	}

	public String getEmail(){
		return email;
	}

	public String getStatus(){
		return status;
	}
}
